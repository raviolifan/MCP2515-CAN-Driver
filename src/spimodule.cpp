///////////////////////////////////////////
// SPI Driver
//
// Initializes and controls the SPI bus.
//
///////////////////////////////////////////
#include <Arduino.h>
#include <SPI.h>

#include "spimodule.h"

///////////////////////////////////////////
// SPI Pin Assignments
///////////////////////////////////////////
const uint8_t SPI_SCK_PIN  = 18;
const uint8_t SPI_MOSI_PIN = 17;
const uint8_t SPI_MISO_PIN = 16;

///////////////////////////////////////////
// SPI Configuration
///////////////////////////////////////////
SPISettings spiSettings(
    24000000,      // 24 MHz
    MSBFIRST,
    SPI_MODE0);

///////////////////////////////////////////
// SPI Initialization
///////////////////////////////////////////
void spiInit()
{
    SPI.begin(
        SPI_SCK_PIN,
        SPI_MISO_PIN,
        SPI_MOSI_PIN,
        -1
    );
}

///////////////////////////////////////////
// Write One Byte
///////////////////////////////////////////
void spiWrite(uint8_t data)
{
    SPI.transfer(data);
}

///////////////////////////////////////////
// Write Multiple Bytes
///////////////////////////////////////////
void spiWriteBuffer(const uint8_t *buffer, size_t length)
{
    while (length--)
    {
        SPI.transfer(*buffer++);
    }
}

///////////////////////////////////////////
// Begin SPI Transaction
///////////////////////////////////////////
void spiBeginTransaction()
{
    SPI.beginTransaction(spiSettings);
}

///////////////////////////////////////////
// End SPI Transaction
///////////////////////////////////////////
void spiEndTransaction()
{
    SPI.endTransaction();
}

///////////////////////////////////////////
// Read One Byte
///////////////////////////////////////////
uint8_t spiTransfer(uint8_t data)
{
    return SPI.transfer(data);
}

///////////////////////////////////////////
// Read Multiple Bytes
///////////////////////////////////////////
void spiTransferBuffer(uint8_t *buffer, size_t length)
{
    while (length--)
    {
        *buffer++ = SPI.transfer(0x00);        
    }
}